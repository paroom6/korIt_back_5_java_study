package com.study.ch04;

public class Operation09 {
    public static void main(String[] args) {
        /*
        * 증감연산사
        *
        *
        * */
        int i = 0;
        //i++; == i += 1;
        System.out.println(i++);//i++ 다음 변수에서 증감 ++i 이번 식에서 증감
        System.out.println(i);
        System.out.println(i--);
        System.out.println(i);







    }
}
